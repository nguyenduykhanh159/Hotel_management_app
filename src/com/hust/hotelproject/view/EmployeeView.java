    public void reloadTableRoom() {
        List<Room> listRoom = roomController.returnAllRoom();
        DefaultTableModel model = (DefaultTableModel) roomTabel.getModel();

        // Delete old data
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        // Add new data
        for (Room room : listRoom) {
            model.addRow(new Object[]{room.getRoomId(), room.getName(), room.getType(), room.getPrice(), room.getStatus()});
        }

    }

