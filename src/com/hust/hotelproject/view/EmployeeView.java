    private void findCusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findCusButtonActionPerformed
        // TODO add your handling code here:
        String name = nameCusTxt.getText();
        if (name.isEmpty()) {
            checkNameLabel.setText("Input name");
            return;
        } else {
            checkNameLabel.setText("");
        }

        String phone = phoneCusTxt.getText();
        if (phone.isEmpty()) {
            checkPhoneLabel.setText("Input Phone");
            return;
        } else {
            checkPhoneLabel.setText("");
        }

        String customerId = IdGenerator.generateEmployeeId(4);
        Customer customer = new Customer(customerId, name, addressCusTxt.getText(), IndentityCusTxt.getText(), phone);

        booking.setBookId(IdGenerator.generateEmployeeId(6));
        booking.setCustomerId(customerId);
        booking.setCustomerName(name);

        String employeeId = userLogin.getEmployeeId();
        Logger.info(employeeId);
        booking.setEmployeeId(employeeId);

        String day = dayCusTxt.getText();
        if (day.isEmpty() || day.length() < 2) {
            checkDateLabel.setText("Day format wrong");
            return;
        }
        String month = monthCusTxt.getText();
        if (month.isEmpty() || month.length() < 2) {
            checkDateLabel.setText("Month format wrong");
            return;
        }
        String date = yearCusTxt.getText() + "-" + month + "-" + day;

        if (!isValid(date)) {
            checkDateLabel.setText("Date wrong");
            return;
        }

        if (java.time.LocalDate.now().toString().compareTo(date) >= 0) {
            checkDateLabel.setText("Date wrong");
            return;
        }
        Date dateCheckOut = Date.valueOf(date);
        booking.setTimeCheckOut(dateCheckOut);

        //Check input
        if (booking.getRoomID() == null) {
            JOptionPane.showMessageDialog(this, "Room not selected");
            return;
        }

        if (booking.getServiceId() == null) {
            JOptionPane.showMessageDialog(this, "Service not selected");
            return;
        }

        boolean check = customerController.insertCustomer(customer);
        if (check) {
            Logger.info("Customer Insert success");
        } else {
            Logger.error("Insert false");
        }

        check = bookingController.insertBooking(booking);
        if (check) {
            Logger.info("Booking Insert success");
        } else {
            Logger.error("Insert false");
        }
        reloadTableBooking();


    }//GEN-LAST:event_findCusButtonActionPerformed

